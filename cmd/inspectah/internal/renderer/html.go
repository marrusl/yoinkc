// reportJS returns minimal JavaScript for the HTML report.
func reportJS() string {
	return `
document.querySelectorAll('details summary').forEach(s => {
  s.addEventListener('click', () => {
    const d = s.parentElement;
    d.open = !d.open;
  });
});
`
}
